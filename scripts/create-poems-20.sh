下阕总结战役——"七百里驱十五日"概括了横扫的时空，"横扫千军如卷席"以"卷席"比喻摧枯拉朽之势。"有人泣，为营步步嗟何及"以讥讽收尾，嘲笑敌人"步步为营"的战术彻底破产，只剩哭泣。
