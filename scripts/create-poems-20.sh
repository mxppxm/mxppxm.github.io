1934年夏，毛泽东在江西会昌。此时他已被排挤出中央领导核心，仅挂名苏维埃政府主席。第五次反"围剿"形势危急，长征即将开始。然而这首词却出人意料地明朗开阔——"莫道君行早""风景这边独好"，在最困难的时候保持着乐观。

