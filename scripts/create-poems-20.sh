# 采桑子·重阳

**一九二九年十月**

人生易老天难老，岁岁重阳。
今又重阳，战地黄花分外香。

一年一度秋风劲，不似春光。
胜似春光，寥廓江天万里霜。

---

## 创作背景

1929年重阳节，毛泽东在闽西山区。此前他因疟疾大病一场，几乎丧命，且因党内分歧离开了红四军领导岗位，处境艰难。他却在这个重阳节写出了最豁达的词作，展现了超越个人荣辱的胸襟。

## 赏析

上阕从人生与天道的对比切入——"人生易老天难老"化用李贺"天若有情天亦老"，反其意而强调自然的永恒。"战地黄花分外香"是全词的"诗眼"——战场上的野菊花比和平时期的更香，因为它是用鲜血浇灌的，蕴含着革命者对生命意义的独特理解。

下阕写秋风。"不似春光"与"胜似春光"的转折出人意表——秋风虽冷，但"寥廓江天万里霜"的开阔景象胜过春日的柔媚，暗喻革命道路虽苦却壮丽。
EOF

