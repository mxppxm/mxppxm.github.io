# 贺新郎·别友

**一九二三年**

挥手从兹去。
更那堪凄然相向，苦情重诉。
眼角眉梢都似恨，热泪欲零还住。
知误会前番书语。
过眼滔滔云共雾，算人间知己吾和汝。
人有病，天知否？

今朝霜重东门路。
照横塘半天残月，凄清如许。
汽笛一声肠已断，从此天涯孤旅。
凭割断愁丝恨缕。
要似昆仑崩绝壁，又恰像台风扫寰宇。
重比翼，和云翥。

---

## 创作背景

1923年底，毛泽东离开长沙前往广州参加国民党第一次全国代表大会，与妻子杨开慧分别。此前两人因革命工作聚少离多，词中"知误会前番书语"透露出夫妻间曾有书信上的误会。"别友"实为别妻——以"友"相称，寓夫妻之情于革命同志之义。

## 赏析

这是毛泽东诗词中少见的婉约之作。上阕写离别之痛，"眼角眉梢都似恨，热泪欲零还住"细腻地刻画了杨开慧强忍泪水的神情。"算人间知己吾和汝"是整首词最深情的一句。

下阕写离别场景——东门霜重、残月横塘、汽笛肠断，意象凄清。但结尾陡然转折——"要似昆仑崩绝壁"将个人离愁升华为革命豪情，"重比翼，和云翥"寄托了重逢的期待。
EOF

