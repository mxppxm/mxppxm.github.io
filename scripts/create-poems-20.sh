# 七绝·改西乡隆盛诗赠父亲

**一九一〇年**

孩儿立志出乡关，学不成名誓不还。
埋骨何须桑梓地，人生无处不青山。

---

## 创作背景

1910年秋，17岁的毛泽东考入湘乡县立东山高等小学堂。离家之前，他将这首诗夹在父亲的账簿里，以此明志。原诗取自日本西乡隆盛，经毛泽东改写后注入了一代少年志在四方的豪情。

## 赏析

首句"立志出乡关"直抒胸臆，少年毛泽东不甘困守山村，志在远方。"学不成名誓不还"语气决绝。后两句视野开阔——"埋骨何须桑梓地"超越传统归葬故土的观念，"人生无处不青山"以青山作结，将个体命运融入天地之间。
EOF

