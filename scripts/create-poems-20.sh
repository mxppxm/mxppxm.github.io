上阕以黎明起兴——"东方欲晓，莫道君行早"化用旧谚而翻新意：天将破晓，别说你起得最早，还有人为革命付出了更多。"踏遍青山人未老"是毛泽东对自己半生革命征程的总结，虽历经坎坷而斗志不衰。"风景这边独好"更是困境中的坚守。

下阕写登高远眺——"会昌城外高峰，颠连直接东溟"，视野从赣南山峰一直延伸到东海，气势开阔。结尾以"更加郁郁葱葱"收束，一幅生机勃勃的画面，全无失意之情。
EOF

