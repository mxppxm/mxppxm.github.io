## 赏析

上阕开篇"万木霜天红烂漫"以浓墨重彩描摹冬日山林——霜叶如火，暗合战火。"齐声唤，前头捉了张辉瓒"以口语入词，再现了战场上活捉敌将的欢呼场面，这在传统词作中极为罕见。

下阕"二十万军重入赣"写敌人卷土重来，"唤起工农千百万"是对第二次反围剿的动员。"不周山下红旗乱"借用共工怒触不周山的神话——毛泽东在自注中特别为共工正名，将他视为革命造反的英雄。红旗在不周山下翻飞，寓意工农革命终将翻天覆地。
