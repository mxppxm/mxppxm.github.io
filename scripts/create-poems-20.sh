# 西江月·井冈山

**一九二八年秋**

山下旌旗在望，山头鼓角相闻。
敌军围困万千重，我自岿然不动。

早已森严壁垒，更加众志成城。
黄洋界上炮声隆，报道敌军宵遁。

---

## 创作背景

1928年8月，湘赣两省的国民党军队趁毛泽东率部在外之机，向井冈山发动"会剿"。留守部队在黄洋界凭险据守，仅用一门迫击炮和三发炮弹就击退了敌军。毛泽东返回后听说此役，欣然命笔。黄洋界保卫战是井冈山斗争时期以少胜多的经典战例。

## 赏析

上阕以"旌旗""鼓角"营造战场氛围，"敌军围困万千重"渲染敌众我寡的危急态势，"我自岿然不动"以四两拨千斤的从容回应——一个"自"字写出无比的自信。

下阕揭示胜利原因："森严壁垒"是军事准备，"众志成城"是精神力量。"黄洋界上炮声隆，报道敌军宵遁"——炮声一响，敌人连夜逃跑，写得轻松幽默，尽显胜者的从容。
EOF

