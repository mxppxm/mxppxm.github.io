# 卜算子·咏梅

**一九六一年十二月**

*读陆游咏梅词，反其意而用之。*

风雨送春归，飞雪迎春到。
已是悬崖百丈冰，犹有花枝俏。

俏也不争春，只把春来报。
待到山花烂漫时，她在丛中笑。

---

## 创作背景

1961年底，中国正处于三年困难时期，同时面临中苏交恶的国际压力。毛泽东读陆游《卜算子·咏梅》后"反其意而用之"，以梅花象征中国共产党人在逆境中坚韧不屈的品格。

## 赏析

陆游的咏梅词是"零落成泥碾作尘，只有香如故"的孤芳自赏；毛泽东则反其道行之——梅花不是悲苦的，而是积极的。

上阕写梅花在严寒中绽放："悬崖百丈冰"极言环境之恶劣，"犹有花枝俏"突出梅花的不屈——一个"俏"字写出了梅花傲雪的风姿。

下阕写梅花的品格："俏也不争春"表明梅花不慕虚荣，"只把春来报"写它的使命——在寒冬中传递春天的消息。"待到山花烂漫时，她在丛中笑"是最动人的收束：当百花盛开，梅花并不居功，而是隐在花丛中微笑。这种谦逊与乐观，正是毛泽东对那个艰难年代的精神回应。
EOF

