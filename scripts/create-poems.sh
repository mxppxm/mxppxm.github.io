1965年5月，毛泽东重上井冈山，距1927年他在此创建第一个农村革命根据地已过去38年。面对井冈山的崭新面貌，感慨万千，写下这首词。

## 赏析

上阕写重游故地的喜悦。"旧貌变新颜"概括了38年间的巨变，"到处莺歌燕舞"描绘了和平时期的繁荣景象，"高路入云端"既是实景也暗喻建设成就。"过了黄洋界，险处不须看"——经历了革命最艰难的阶段，如今已可以从容面对一切。

下阕回顾历史、展望未来。"三十八年过去，弹指一挥间"极言时光飞逝。"可上九天揽月，可下五洋捉鳖"是毛泽东最具豪情的句子之一。末句"世上无难事，只要肯登攀"已成为广为流传的格言。
EOF

