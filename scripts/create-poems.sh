安得倚天抽宝剑，把汝裁为三截？
一截遗欧，一截赠美，一截还东国。
太平世界，环球同此凉热。

---

## 创作背景

1935年10月，中央红军翻越岷山，长征即将结束。毛泽东远望昆仑山脉，以超越时空的视角审视这座"万山之祖"，写下了这首充满国际主义情怀的词作。作者自注："昆仑各脉之积雪，融化时江河横溢，或为灾患……"

## 赏析

上阕写昆仑的雄伟与"功罪"。"横空出世"四字开篇，气势逼人。"飞起玉龙三百万"以夸张手法描绘雪山群峰，"周天寒彻"写其凛冽。但积雪融化后江河泛滥成灾——"人或为鱼鳖"道出了昆仑的"罪"。"千秋功罪，谁人曾与评说？"以反问作结，将自然景观提升到哲学层面。

下阕以主宰者的姿态对话昆仑。"不要这高，不要这多雪"是对自然的改造愿望。"安得倚天抽宝剑，把汝裁为三截"——这是何等想象！将昆仑裁为三段，分赠欧洲、美洲、东方，让全世界共享清凉。"太平世界，环球同此凉热"表达了人类命运共同体的愿景，是毛泽东诗词中罕见的国际主义表达。
