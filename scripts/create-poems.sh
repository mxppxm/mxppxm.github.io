---

## 创作背景

1949年4月23日，中国人民解放军攻占南京，宣告了国民党反动统治的覆灭。毛泽东在北京香山双清别墅得知这一消息后，欣然写下此诗。

## 赏析

首联以"钟山风雨"起笔，"百万雄师过大江"气势恢宏，描绘了渡江战役的壮阔场面。

颔联"虎踞龙盘今胜昔，天翻地覆慨而慷"，既写南京城的新生，也概括了一个时代的巨变。

颈联化用项羽典故，"宜将剩勇追穷寇"表达了将革命进行到底的决心——不能像项羽那样"沽名钓誉"放走刘邦。

尾联"天若有情天亦老，人间正道是沧桑"是千古名句，将历史的沧桑感与哲学思辨融为一体，道出了社会变革是不可阻挡的历史规律。
EOF

