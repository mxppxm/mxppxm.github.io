# 水调歌头·游泳

**一九五六年六月**

才饮长沙水，又食武昌鱼。
万里长江横渡，极目楚天舒。
不管风吹浪打，胜似闲庭信步，今日得宽馀。
子在川上曰：逝者如斯夫！

风樯动，龟蛇静，起宏图。
一桥飞架南北，天堑变通途。
更立西江石壁，截断巫山云雨，高峡出平湖。
神女应无恙，当惊世界殊。

---

## 创作背景

1956年6月，毛泽东视察武汉，三次畅游长江。当时武汉长江大桥正在建设中（1957年通车），三峡工程也已在构想之中。这首词既记录了游泳的畅快感受，也展现了对新中国建设蓝图的豪迈想象。

## 赏析

上阕写游泳体验。"万里长江横渡"是何等气魄，"不管风吹浪打，胜似闲庭信步"将搏击风浪写得从容自若。末尾化用《论语》"逝者如斯夫"，从体育运动过渡到对时间流逝的感慨。

下阕转入建设宏图。"一桥飞架南北，天堑变通途"写武汉长江大桥的壮举。"高峡出平湖"则是对三峡大坝的预见——这一设想在47年后的2003年终于成为现实。末句借巫山神女的视角，惊叹世界变化之大。
EOF

