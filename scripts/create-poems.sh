# 忆秦娥·娄山关

**一九三五年二月**

西风烈，长空雁叫霜晨月。
霜晨月，马蹄声碎，喇叭声咽。

雄关漫道真如铁，而今迈步从头越。
从头越，苍山如海，残阳如血。

---

## 创作背景

1935年2月，遵义会议后红军二渡赤水，回师贵州，攻克娄山关，取得长征以来的第一次重大胜利。毛泽东在战斗结束后写下这首词，这也是他在遵义会议重新掌握军事指挥权后的第一首作品。

## 赏析

上阕写拂晓行军：西风凛冽，雁叫长空，霜晨冷月——"马蹄声碎"写出急行军的紧张，"喇叭声咽"以低沉的军号声烘托出征途的艰辛。"碎"和"咽"两字极为传神。

下阕写攻克雄关。"雄关漫道真如铁"极言娄山关之险峻，"而今迈步从头越"展现红军不畏任何艰难的英雄气概。叠句"从头越"既是跨越雄关的动作，也暗含遵义会议后"从头开始"的深意。末尾"苍山如海，残阳如血"以壮阔的视觉意象作结，被誉为中国诗词史上最壮美的落日描写之一。
EOF

