# 念奴娇·昆仑

**一九三五年十月**

横空出世，莽昆仑，阅尽人间春色。
飞起玉龙三百万，搅得周天寒彻。
夏日消溶，江河横溢，人或为鱼鳖。
千秋功罪，谁人曾与评说？

