# 七律·长征

**一九三五年十月**

红军不怕远征难，万水千山只等闲。
五岭逶迤腾细浪，乌蒙磅礴走泥丸。
金沙水拍云崖暖，大渡桥横铁索寒。
更喜岷山千里雪，三军过后尽开颜。

---

## 创作背景

1935年10月，中央红军即将完成长征。从1934年10月出发以来，红军历经千难万险——翻越五岭、穿越乌蒙山、巧渡金沙江、强渡大渡河、飞夺泸定桥、翻越岷山。毛泽东在长征即将胜利之际写下这首诗，高度概括了红军长征的壮烈历程。

## 赏析

首联"不怕"与"只等闲"定下全诗基调——以轻描淡写的语气对待万里征途的艰险，彰显红军的英雄气概。

颔联运用夸张手法，将"五岭逶迤"比作"细浪"，将"乌蒙磅礴"视作"泥丸"，以小喻大，气吞山河。

颈联一"暖"一"寒"，对比鲜明：金沙江的巧渡是喜悦，大渡河的铁索是惊险，冷暖交织，极富张力。

尾联"更喜"与"尽开颜"将全诗推向高潮——翻过最后的岷山千里雪，三军将士终于展露笑颜。
EOF

