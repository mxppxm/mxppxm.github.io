# 沁园春·长沙

**一九二五年**

独立寒秋，湘江北去，橘子洲头。
看万山红遍，层林尽染；
漫江碧透，百舸争流。
鹰击长空，鱼翔浅底，万类霜天竞自由。
怅寥廓，问苍茫大地，谁主沉浮？

携来百侣曾游，忆往昔峥嵘岁月稠。
恰同学少年，风华正茂；
书生意气，挥斥方遒。
指点江山，激扬文字，粪土当年万户侯。
曾记否，到中流击水，浪遏飞舟？

---

## 创作背景

1925年深秋，毛泽东离开故乡韶山，前往广州主持农民运动讲习所。途经长沙，重游橘子洲，面对湘江秋色，回忆起与战友在此求学的岁月，写下这首气势磅礴的词。

## 赏析

上阕写景，描绘了一幅色彩斑斓的湘江秋景图——"万山红遍""漫江碧透""鹰击长空""鱼翔浅底"，动静结合，远近交错，以"问苍茫大地，谁主沉浮"的哲思作结，将自然景观升华为对时代命运的叩问。

下阕追忆，"恰同学少年"的意气风发与"粪土当年万户侯"的豪情壮志交织，"到中流击水，浪遏飞舟"既是少年时代的真实写照，也象征着搏击时代洪流的精神姿态。
EOF

