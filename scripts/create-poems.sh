# 沁园春·雪

**一九三六年二月**

北国风光，千里冰封，万里雪飘。
望长城内外，惟余莽莽；
大河上下，顿失滔滔。
山舞银蛇，原驰蜡象，欲与天公试比高。
须晴日，看红装素裹，分外妖娆。

江山如此多娇，引无数英雄竞折腰。
惜秦皇汉武，略输文采；
唐宗宋祖，稍逊风骚。
一代天骄，成吉思汗，只识弯弓射大雕。
俱往矣，数风流人物，还看今朝。

---

## 创作背景

1936年2月，红军完成东征，毛泽东率部抵达陕北清涧县袁家沟。时值大雪，他登高远望，面对千里雪原，写下了这首后来震动文坛的词作。1945年重庆谈判期间，此词在文化界广泛传抄，引起巨大轰动。

## 赏析

上阕写北国雪景，从"千里""万里"的宏观视角，到"山舞银蛇""原驰蜡象"的动态描写，将静态的冰雪世界赋予蓬勃的生命力。"须晴日"一句更将想象延伸至雪后晴天，红白交映，妖娆至极。

下阕纵论千古英雄，以"惜"字统领对秦皇、汉武、唐宗、宋祖、成吉思汗的品评——承认其历史功绩，也指出其局限。末句"数风流人物，还看今朝"将视野从历史拉回当下，气度非凡。
EOF

