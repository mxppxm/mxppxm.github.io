## 赏析

上阕写登楼所见：长江茫茫九派、铁路沉沉一线，"烟雨莽苍苍"既是实景也暗喻时局晦暗不明。"龟蛇锁大江"——龟山蛇山夹江而立，一个"锁"字写出局势的紧迫。

下阕化用崔颢《黄鹤楼》"黄鹤一去不复返"的典故，以"把酒酹滔滔"的祭祀动作表达对革命的誓言。末句"心潮逐浪高"将内心激荡与长江波涛融为一体，在低沉中透出昂扬。
